def get_carbs(food_name: str) -> float:
    """获取食物的碳水含量"""
    if food_name in CARBS_DATABASE:
        return CARBS_DATABASE[food_name]
    
    # 模糊匹配
    food_name = food_name.lower()
    for name, carbs in CARBS_DATABASE.items():
        if food_name in name.lower() or name.lower() in food_name:
            return carbs
    
    return None
